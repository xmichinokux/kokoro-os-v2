/**
 * テンプレートからエミの一言を選択する
 * lastLine と同じものは避ける
 */
export function pickEmiLine(
  type: InsightType,
  level: InsightLevel,
  lastLine?: string,
): string {
  const templates = EMI_TEMPLATES[type][level];
  const candidates = lastLine
    ? templates.filter(t => t !== lastLine)
    : templates;

  // 全部 lastLine と同じだった場合はフルリストから選択
  const pool = candidates.length > 0 ? candidates : templates;
  return pool[Math.floor(Math.random() * pool.length)];
}
